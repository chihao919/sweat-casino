import { collectData } from './agents/data-collector.mjs'
import { analyze } from './agents/analyst.mjs'
import { buildContext } from './agents/copywriter.mjs'
import { writeFileSync, mkdirSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..')

function divider(char = 'вФА', len = 44) { return char.repeat(len) }

async function run() {
  const startTime = Date.now()

  console.log('\n' + divider('вХР'))
  console.log('  рЯО∞  ж±Чж∞іи≥≠е†і Daily Motivator')
  console.log(divider('вХР'))
  console.log(`  ${new Date().toLocaleString('zh-TW', { timeZone: 'Asia/Taipei' })}`)
  console.log(divider('вФА'))

  // Agent 1: Data Collection
  console.log('\nрЯУК [Agent 1 / Data Collector] Fetching player data...')
  let data
  try {
    data = await collectData()
  } catch (err) {
    console.error(`\nвЭМ Data collection failed: ${err.message}`)
    console.error('   вЖТ Check NEXT_PUBLIC_SUPABASE_URL, SUPABASE_SERVICE_ROLE_KEY in .env')
    console.error('   вЖТ Or run: node scripts/discover-schema.mjs to inspect tables')
    process.exit(1)
  }

  const { players, teamStats } = data
  console.log(`   вЬУ ${players.length} players loaded`)
  console.log(`   рЯРВ Red Bull: ${teamStats.red.players.length} players, ${teamStats.red.totalKm.toFixed(1)} km this week`)
  console.log(`   рЯРївАНвЭДпЄП White Bear: ${teamStats.white.players.length} players, ${teamStats.white.totalKm.toFixed(1)} km this week`)

  // Agent 2: Analysis
  console.log('\nрЯІ† [Agent 2 / Analyst] Analyzing progress...')
  const analysis = analyze(data)
  const { categories, teamLeader, daysLeft, activePlayers } = analysis
  console.log(`   Days left this week: ${daysLeft}`)
  console.log(`   Active players: ${activePlayers}/${players.length}`)
  console.log(`   рЯФі Danger zone: ${categories.danger.length} players`)
  console.log(`   вЪ†пЄП  Behind pace: ${categories.behind.length} players`)
  console.log(`   рЯПЖ Crushers: ${categories.crushers.length} players`)
  console.log(`   Team leader: ${teamLeader === 'red' ? 'рЯРВ Red Bull' : 'рЯРївАНвЭДпЄП White Bear'}`)

  // Output context for Claude Code to generate messages
  const context = buildContext(analysis)

  console.log('\n' + divider('вХР'))
  console.log('  рЯУЛ  Copywriter ContextпЉИдЊЫ Claude зФҐзФЯ LINE и®КжБѓпЉЙ')
  console.log(divider('вХР'))
  console.log(context)
  console.log(divider('вФА'))

  // Save report (without messages вАФ those will be generated by Claude Code)
  const today = new Date().toISOString().split('T')[0]
  const reportDir = join(ROOT, 'reports')
  mkdirSync(reportDir, { recursive: true })
  const report = {
    date: today,
    generated_at: new Date().toISOString(),
    summary: {
      total_players: players.length,
      active_players: activePlayers,
      days_left: daysLeft,
      danger_count: categories.danger.length,
      behind_count: categories.behind.length,
      crusher_count: categories.crushers.length,
      team_leader: teamLeader,
    },
    context,
    raw: { players, teamStats: analysis.teamStats },
  }
  const reportPath = join(reportDir, `${today}.json`)
  writeFileSync(reportPath, JSON.stringify(report, null, 2))

  const elapsed = ((Date.now() - startTime) / 1000).toFixed(1)
  console.log(`\nвЬЕ Done in ${elapsed}s  |  Saved: reports/${today}.json`)
  console.log(divider('вФА') + '\n')
}

run()
